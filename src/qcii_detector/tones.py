FDMA_TONES_HZ = [bucket[2] for bucket in FDMA_TONE_BUCKETS]
TDMA_TONES_HZ = [bucket[2] for bucket in TDMA_TONE_BUCKETS]

QCII_TONE_SETS = {
    "fdma": FDMA_TONES_HZ,
    "tdma": TDMA_TONES_HZ,
}

QCII_TONE_BUCKET_SETS = {
    "fdma": FDMA_TONE_BUCKETS,
    "tdma": TDMA_TONE_BUCKETS,
}


def get_tone_set(name: str = "fdma") -> list[float]:
    """Return the requested tone set or raise for unknown names."""
    key = name.lower()
    if key not in QCII_TONE_SETS:
        raise ValueError(f"Unknown tone set: {name}. Valid sets: {', '.join(sorted(QCII_TONE_SETS))}")
    return QCII_TONE_SETS[key]


def get_tone_buckets(name: str = "fdma") -> list[tuple[float, float, float]]:
    """Return the requested tone bucket set or raise for unknown names."""
    key = name.lower()
    if key not in QCII_TONE_BUCKET_SETS:
        raise ValueError(f"Unknown tone set: {name}. Valid sets: {', '.join(sorted(QCII_TONE_BUCKET_SETS))}")
    return QCII_TONE_BUCKET_SETS[key]


def decode_standard(freq_hz: float, tone_set: str = "fdma") -> float | None:
    """Return the decoded standard tone for a received frequency in the set's bucket map."""
    for lower_hz, upper_hz, decoded_hz in get_tone_buckets(tone_set):
        if lower_hz <= freq_hz <= upper_hz:
            return decoded_hz
    return None


def infer_tone_set_for_pair(tone_a_hz: float, tone_b_hz: float) -> str | None:
    """Infer whether a pair belongs to the FDMA or TDMA standard decoded tone set."""
    matches = []
    for tone_set, tones in QCII_TONE_SETS.items():
        if tone_a_hz in tones and tone_b_hz in tones:
            matches.append(tone_set)
    if len(matches) == 1:
        return matches[0]
    return None


def nearest_standard(freq_hz: float, tone_set: str = "fdma") -> float | None:
    """Return the decoded standard frequency for a received tone within the set's valid buckets."""
    return decode_standard(freq_hz, tone_set)
